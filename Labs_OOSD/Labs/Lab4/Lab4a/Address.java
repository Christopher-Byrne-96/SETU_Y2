package Lab4a;

public class Address {
	
	//-----------------------------------------------------------------------------------------------------------------------
	//|||||| VARIABLES    |||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||
	//-----------------------------------------------------------------------------------------------------------------------

	private String street;
	private String cityTown;
	private String county;
	
	//-----------------------------------------------------------------------------------------------------------------------
	//|||||| CONSTRUCTERS |||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||
	//-----------------------------------------------------------------------------------------------------------------------
	
	public Address() {
		this.setStreet("Unknown");
		this.setCityTown("Unknown");
		this.setCounty("Unknown");
	}
	
	public Address(String strt) {
		this.setStreet(strt);
		this.setCityTown("Unknown");
		this.setCounty("Unknown");
	}
	
	public Address(String strt, String ctytwn) {
		this.setStreet(strt);
		this.setCityTown(ctytwn);
		this.setCounty("Unknown");
	}
	
	public Address(String strt, String ctytwn, String cnty) {
		this.setStreet(strt);
		this.setCityTown(ctytwn);
		this.setCounty(cnty);
	}
	
	//-----------------------------------------------------------------------------------------------------------------------
	//|||||| SETTERS      |||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||
	//-----------------------------------------------------------------------------------------------------------------------
	
	public void setStreet(String strt) {
		street = strt;
	}
	
	public void setCityTown(String ctytwn) {
		cityTown = ctytwn;
	}
	
	public void setCounty(String cnty) {
		county = cnty;
	}
	
	//-----------------------------------------------------------------------------------------------------------------------
	//|||||| GETTERS      |||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||
	//-----------------------------------------------------------------------------------------------------------------------
	
	public String getStreet() {
		return street;
	}
	
	public String getCityTown() {
		return cityTown;
	}
	
	public String getCounty() {
		return county;
	}
	
	//-----------------------------------------------------------------------------------------------------------------------
	//|||||| TO STRING    |||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||
	//-----------------------------------------------------------------------------------------------------------------------
	
	public String toString() {
		return street + "," + cityTown + "," + county;
	}
	
}
