package Lab4a;

public class Office {
	
	//-----------------------------------------------------------------------------------------------------------------------
	//|||||| VARIABLES    |||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||
	//-----------------------------------------------------------------------------------------------------------------------

	private static int officeRooms = 100;
	private int roomNum;
	private int employeesAssigned;
	private Employee[] employees = new Employee[2];
	
	//-----------------------------------------------------------------------------------------------------------------------
	//|||||| CONSTRUCTORS |||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||
	//-----------------------------------------------------------------------------------------------------------------------
	
	public Office() {
		
		roomNum = officeRooms;
		officeRooms++;
		employeesAssigned = 0;
	}
	
	//-----------------------------------------------------------------------------------------------------------------------
	//|||||| SETTERS      |||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||
	//-----------------------------------------------------------------------------------------------------------------------
	
	public void setEmployee(Employee newEmployee) {
		
		employees[employeesAssigned] = newEmployee;
		employeesAssigned++;
	}
	
	//-----------------------------------------------------------------------------------------------------------------------
	//|||||| GETTERS      |||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||
	//-----------------------------------------------------------------------------------------------------------------------
	
	public int getNumofEmployees() {
		
		return employeesAssigned;
	}
	
	public void getEmployees() {
		
		for(int i = 0; i < employeesAssigned; i++) {
			System.out.println(employees[i].toString());
		}
	}
	
	//-----------------------------------------------------------------------------------------------------------------------
	//|||||| TO STRING    |||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||
	//-----------------------------------------------------------------------------------------------------------------------
	
	public String toString() {
		
		return "ROOM NUM   :: " + roomNum + "\r\n" +
			   "EMPLOYEES  :: " + employeesAssigned ;
	}
}
