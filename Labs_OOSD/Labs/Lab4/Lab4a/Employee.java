package Lab4a;

public class Employee {
	
	//-----------------------------------------------------------------------------------------------------------------------
	//|||||| VARIABLES    |||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||
	//-----------------------------------------------------------------------------------------------------------------------

	private static int employeeNumbers = 1000;
	private int employeeID;
	private String name;
	private String employeeType;
	private Address address;
	private Office office;
	private String car = "Audi A6";
	
	//-----------------------------------------------------------------------------------------------------------------------
	//|||||| CONSTRUCTORS |||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||
	//-----------------------------------------------------------------------------------------------------------------------
	
	public Employee() {
		
	}
	
	public Employee(Address x, String type, String empName) {
		
		employeeID = employeeNumbers;
		employeeNumbers++;
		name = empName;
		employeeType = type;
		address = x;
	}
	
	//-----------------------------------------------------------------------------------------------------------------------
	//|||||| SETTERS      |||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||
	//-----------------------------------------------------------------------------------------------------------------------
	
	public void setName(String empName) {
		
		name = empName;
	}
	
	public void setAddress(Address x) {
		
		address = x;
	}
	
	public void setType(String type) {
		
		employeeType = type;
	}
	
	public void setOffice(Office x) {
		office = x;
	}
	
	public void setCar(String x) {
		
		car = x;
	}
	
	//-----------------------------------------------------------------------------------------------------------------------
	//|||||| GETTERS      |||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||
	//-----------------------------------------------------------------------------------------------------------------------
	
	public String getName() {
		
		return name;
	}
	
	public Address getAddress() {
		
		return address;
	}
	
	public String getType() {
		
		return employeeType;
	}
	
	public Office getOffice() {
		
		return office;
	}
	
	public String getCar() {
		
		return car;
	}
	
	//-----------------------------------------------------------------------------------------------------------------------
	//|||||| TO STRING    |||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||
	//-----------------------------------------------------------------------------------------------------------------------
	
	public String toString() {
		
		if(employeeType.equalsIgnoreCase("manager")) {
		return "=================================================================================" +
			   "EMPLOYEE ID     :: "    + employeeID  + "\r\n" +
			   "NAME            :: "    + name + "\r\n" +
			   "ADDRESS         :: "    + address + "\r\n" +
		       "OFFICE          :: "	+ office + "\r\n" +
		       "POSITION        :: "    + employeeType + "\r\n" +
		       "COMPANY CAR     :: "	+ car + "\r\n" +
		       "=================================================================================";
		}
		else {
			return     "=================================================================================" +
					   "EMPLOYEE ID     :: "    + employeeID  + "\r\n" +
					   "NAME            :: "    + name + "\r\n" +
					   "ADDRESS         :: "    + address + "\r\n" +
				       "OFFICE          :: "	+ office + "\r\n" +
				       "POSITION        :: "    + employeeType + "\r\n" +
				       "=================================================================================";
		}
	}
	
	//-----------------------------------------------------------------------------------------------------------------------
	//|||||| EXTRA METHODS ||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||||
	//-----------------------------------------------------------------------------------------------------------------------
	
	public int getEmployessRecords() {
		
		return employeeNumbers - 1000;
	}
	
}
